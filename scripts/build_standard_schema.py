def get_content(a):
    c = a.get("조문내용", "")
    if isinstance(c, list):
        parts = []
        for item in c:
            if isinstance(item, str):
                parts.append(item)
            elif isinstance(item, list):
                for sub in item:
                    if isinstance(sub, str):
                        parts.append(sub)
        return "\n".join(parts)
    return str(c) if c else ""


